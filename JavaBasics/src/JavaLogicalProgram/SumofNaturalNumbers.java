package JavaLogicalProgram;

public class SumofNaturalNumbers {

	public static void main(String[] args) {
		// 13 Java Program to Calculate the Sum of Natural Numbers
		
		int num = 100;
		int sum =0;
		
		for(int i=1;i<=num;i++)
		{
			sum = sum+i;			
		}
		System.out.println("Sum is ="+ sum);

	}

}
