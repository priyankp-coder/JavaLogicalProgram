package JavaLogicalProgram;

public class TableMultiplication {

	public static void main(String[] args) {
		// 15 Java Program to Generate Multiplication Table
		//2 x 1 = 2
		//2 x 2 = 4
		
		int num = 18;
		
		for(int i = 1; i<=10;i++)
		{
			System.out.println(num+" x "+ i+ " = "+(num*i));
		}

	}

}
